package com.trustbank.Model;

import java.io.Serializable;

public class AtmModel implements Serializable{
    private String terminalCode, state, city, location, pincode, branchName, latitude,
            longitute, ifscCode, micrCode,contact_no, branch_manager, branch_manager_mob;


    public AtmModel() {
    }

    public String getTerminalCode() {
        return terminalCode;
    }

    public void setTerminalCode(String terminalCode) {
        this.terminalCode = terminalCode;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getPincode() {
        return pincode;
    }

    public void setPincode(String pincode) {
        this.pincode = pincode;
    }

    public String getBranchName() {
        return branchName;
    }

    public void setBranchName(String branchName) {
        this.branchName = branchName;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitute() {
        return longitute;
    }

    public void setLongitute(String longitute) {
        this.longitute = longitute;
    }

    public String getIfscCode() {
        return ifscCode;
    }

    public void setIfscCode(String ifscCode) {
        this.ifscCode = ifscCode;
    }

    public String getMicrCode() {
        return micrCode;
    }

    public void setMicrCode(String micrCode) {
        this.micrCode = micrCode;
    }

    public String getContact_no() {
        return contact_no;
    }

    public void setContact_no(String contact_no) {
        this.contact_no = contact_no;
    }

    public String getBranch_manager() {
        return branch_manager;
    }

    public void setBranch_manager(String branch_manager) {
        this.branch_manager = branch_manager;
    }

    public String getBranch_manager_mob() {
        return branch_manager_mob;
    }

    public void setBranch_manager_mob(String branch_manager_mob) {
        this.branch_manager_mob = branch_manager_mob;
    }

}
